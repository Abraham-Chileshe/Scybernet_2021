      <h4 style="font-family:times new roman">~Scientific Laws</h4>
      <center><img src="img/Law.Jpg" style="margin-top:0.5em; width:100%"></center>
      <p style="margin-top:0.5em">Scientific laws or laws of science are statements, based on repeated experiments or observations, that describe or predict a range of natural phenomena. The term law has diverse usage in many cases (approximate, accurate, broad, or narrow) across all fields of natural science (physics, chemistry, biology, geology, astronomy, etc.). Laws are developed from data and can be further developed through mathematics; in all cases they are directly or indirectly based on empirical evidence. It is generally understood that they implicitly reflect, though they do not explicitly assert, causal relationships fundamental to reality, and are discovered rather than invented.   
      Scientific laws summarize the results of experiments or observations, usually within a certain range of application. In general, the accuracy of a law does not change when a new theory of the relevant phenomenon is worked out, but rather the scope of the law's application, since the mathematics or statement representing the law does not change. As with other kinds of scientific knowledge, laws do not have absolute certainty (as mathematical theorems or identities do), and it is always possible for a law to be contradicted, restricted, or extended by future observations. A law can usually be formulated as one or several statements or equations, so that it can be used to predict the outcome of an experiment, given the circumstances of the processes taking place.</p>
