      <h4 style="font-family:times new roman">~Scientific Theory</h4>
      <center><img src="img/Theory.Jpg" style="margin-top:0.5em; width:100%"></center>
      <p style="margin-top:0.5em">A scientific theory is an explanation of an aspect of the natural world that can be repeatedly tested and verified in accordance with the scientific method, using accepted protocols of observation, measurement, and evaluation of results. Where possible, theories are tested under controlled conditions in an experiment. In circumstances not amenable to experimental testing, theories are evaluated through principles of abductive reasoning. Established scientific theories have withstood rigorous scrutiny and embody scientific knowledge.
      The meaning of the term scientific theory (often contracted to theory for brevity) as used in the disciplines of science is significantly different from the common vernacular usage of theory.In everyday speech, theory can imply an explanation that represents an unsubstantiated and speculative guess, whereas in science it describes an explanation that has been tested and widely accepted as valid. These different usages are comparable to the opposing usages of prediction in science versus common speech, where it denotes a mere hope.
      The strength of a scientific theory is related to the diversity of phenomena it can explain and its simplicity. As additional scientific evidence is gathered, a scientific theory may be modified and ultimately rejected if it cannot be made to fit the new findings; in such circumstances, a more accurate theory is then required. That does not mean that all theories can be fundamentally changed (for example, well established foundational scientific theories such as evolution, heliocentric theory, cell theory, theory of plate tectonics etc.). In certain cases, the less-accurate unmodified scientific theory can still be treated as a theory if it is useful (due to its sheer simplicity) as an approximation under specific conditions. A case in point is Newton's laws of motion, which can serve as an approximation to special relativity at velocities that are small relative to the speed of light.</p>
