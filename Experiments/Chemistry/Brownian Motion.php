      <div class="container">
        <h3 class="w3-blue"><center>Experiment to Demonstrate of Brownian Motion</center></h3>
        <br/>
        <p><b>Aim: </b>To study the random motion of smoke particles or to observe Brownian motion in a smoke cell.</p><br/>
        <p><b>Apparatus</b></p>
        <ol type="a">
          <li><p class="mt-1">Glass cell</p></li>
          <li><p class="mt-1">Source of light</p></li>
          <li><p class="mt-1">Microscope</p></li>
          <li><p class="mt-1">Converging lens</p></li>
          <li><p class="mt-1">Source of smoke</p></li>
        </ol><br/>

        <p><b>Method</b></p>
        <p class="mt-1">A small glass cell (smoke cell) in which smoke has been trapped is viewed through a microscope. A microscope is used because the smoke particles are too tiny to be seen using the naked eye. A converging lens is used to focus light from the lamp into the smoke cell. The experimental arrangement is shown below</p>
        <center><img src="../images/brown.jpg" class="mt-2" style="width:600px; max-width:100%"> </center>        
        <p class="mt-2">When light strikes the smoke particles, they appear as bright points of light under the microscope moving randomly in a zig – zag path. The smoke particles appear as spots of light because they reflect some of the light from the source of light towards the microscope.</p>
        <center><img src="../images/smoke.jpg" class="mt-4" style="width:400px; max-width:100%"> </center> 
        <br/>

        <p><b>Explanation</b></p>
        <p>The zig – zag movement is due to the collision of the smoke particles with invisible air molecules that move about randomly in the smoke cell. This is called Brownian motion.</p><br/>
        
        <p><b>Conclusion</b></p>
        <p>The air molecules are in a continuous random motion colliding with the smoke particles and the walls of the smoke cell</p>
        

