        <p><b>Aim: </b>To demonstrate diffusion in liquids</p><br/>
        <p><b>Apparatus</b></p>
        <ol type="a">
          <li><p class="mt-1">Potassium permanganate (VII)</p></li>
          <li><p class="mt-1">Water</p></li>
          <li><p class="mt-1">Glass Tube</p></li>         
        </ol><br/>

        <p><b>Method</b></p>
        <p class="mt-1">Using a glass tube, place a crystal of potassium per manganate (VII) in the middle of the beaker filled with water. The glass tube is used to ensure that the mixing with water does not occur before the crystal is in place.</p>
        <center><img src="../images/diffusion.jpg" class="mt-2" style="width:500px; max-width:100%"> </center> 
        <p>Leave the beaker undisturbed and observe carefully.</p> <br/> 
        <p><b>Observation</b></p>     
        <p class="mt-2">The crystal of potassium permanganate (VII) dissolves and the purple colour slowly spread to the bottom of the beaker and eventually the colour distributes itself throughout the liquid.</p>
        <center><img src="../images/red_diffusion.jpg" class="mt-4" style="width:400px; max-width:100%"> </center> 
        <br/>

        <p><b>Conclusion</b></p>
        <p>Diffusion has taken place and water turns purple because the particles of potassium permanganate (VII) have diffused to all parts of the water.</p>   

