          <h5 class="w3-teal pt-2 pb-2 pl-3 pr-3"><b>Experiment to Demonstrate Respiration in Germinating Seeds</b></h5>
          
          <p class="mt-3"> <font class="w3-text-teal"><u>Materials:</u></font>two thermal flasks, two thermometers, cotton wool, two sets of bean seeds.</p>
          <p class="mt-2">
            <font class="w3-text-teal"><u>Method</u>:</font>
           <ol>
            <li><p>Soak one set of seeds until they start germinating and boil the other set of seeds</p></li>
            <li><p>Soak both sets of seeds in disinfectant to kill micro organisms and place each set in a separate thermal flask.</p></li>
            <li><p>Set the experiment as shown in the following diagram.</p><br/><center><img src="img/boiled.jpg" style="max-width:100%; width:500px"></center></li><br/>
            <li><p>Read the initial temperature from each of the two thermometers.</p></li>
            <li><p>Leave the setup for four days and read temperatures from the two thermometers again.</p></li>
          </ol>

             <font class="w3-text-teal"><u>Observation: </u></font>
            <p>The temperature in the flask containing boiled seeds will remain the same while the temperature in the flask containing germinating seeds will increase.</p>
            <br/>
             <font class="w3-text-teal"><u>Conclusion</u></font>
            <p>Energy is produced during respiration. Germinating seeds carry out respiration while boiled seeds do not.</p>

        </p><br/><br/>  


        <h5 class="w3-green pt-2 pb-2 pl-3 pr-3"><b>Experiment to Demonstrate Respiration in Soil Organisms</b></h5>
          <p class="mt-3"> <font class="w3-text-green"><u>Materials:</u></font>Two large bottles, wire gauze, sodium hydroxide solution, two capillary tubes, two glass tubes, two petri dishes, two samples of soil, two screw valves.</p>
          <p class="mt-2">
            <font class="w3-text-green"><u>Method</u>:</font>
           <ol>
            <li><p>Heat one of the soil samples and leave the other one without heating.</p></li>
            <li><p>Set up the experiment as shown in the following diagram</p><br/><center><img src="img/soil.jpg" style="max-width:100%; width:750px"></center></li><br/>
            <li><p>Leave the setup for five hours and observe what happens to the position of the drop in the capillary tube.</p></li>
          </ol>

             <font class="w3-text-green"><u>Observation: </u></font>
            <p>The oil drop in the container having heated soil remains at the same position while the one in the container having unheated soil moves inwards</p>
            <br/>
             <font class="w3-text-green"><u>Conclusion</u></font>
            <p>Soil contains living organisms that carry out respiration and use up oxygen in the process.</p>

        </p><br/><br/>  



