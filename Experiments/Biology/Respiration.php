         <p class="mt-3"> <font class="w3-text-green"><u>Materials:</u></font> Three large bottles, lime water, glass tubes and a small mammal e.g. a rat</p>
          <p class="mt-2">
            <font class="w3-text-green"><u>Method</u>:</font>
           <ol>
            <li><p>Set up the experiment as shown in the following diagram.</p><br/><center><img src="img/rat.jpg" style="max-width:100%; width:500px"></center></li><br/>
            <li><p>Observe and record what happens to the lime water in containers A and C.</p></li>
          </ol>

             <font class="w3-text-green"><u>Observation: </u></font>
            <p>The lime water in the container C turns milky earlier than the one in A.</p>
            <br/>
             <font class="w3-text-green"><u>Conclusion</u></font>
