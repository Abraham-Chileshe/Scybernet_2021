            <li><p>Leave the setup for five hours and observe what happens to the position of the drop in the capillary tube.</p></li>
          </ol>

             <font class="w3-text-green"><u>Observation: </u></font>
            <p>The oil drop in the capillary tube moves inwards</p>
            <br/>
             <font class="w3-text-green"><u>Conclusion</u></font>
