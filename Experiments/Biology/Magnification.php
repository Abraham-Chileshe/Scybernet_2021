           <h4>Magnification Marks</h4>
          <p>
        The formula must be correctly stated as: <br/> 
              Magnification = Size of Image/Drawing Size of Specimen.<br/>
              The substitution must be correctly done with identical units in the numerator and denominator.  A substitution where size of specimen is swapped with size of drawing is wrong and makes the rest of the calculation wrong. When the units in the numerator and denominator are not identical, the substitution is rejected along with the rest of the calculation e.g. if an individual measures the specimen size as being 6.4 cm and the corresponding measurement on the diagram is 7.2 cm, the substitution will be correct if written as: 7.2 cm/6.4cm or 72 mm/64 mm but will be wrong if written as: 7.2/6.4 or 72/64 or 7.2cm/64 mm or 72 mm/6.4 cm.<br/> The final answer for magnification must be written to one decimal place with a multiplication sign (X) or the word "times" either before or after the magnification and without units. e.g. the answer for the substitution given above is 1.125 but should be written as: X1.1 or 1.1X or times 1.1 or 1.1 times.<br/>
