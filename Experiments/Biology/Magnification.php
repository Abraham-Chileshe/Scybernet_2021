        <h3 style="font-family:fantasy"><center>Magnification Activity</center></h3>
        <br/>
        <p>
          <b>AIM</b>: To understand how to use a magnifying lens.<br/>
          <b>MATERIALS</b>: a handlens and a leaf.
          <br/><br/>
          <h4 id="#PRO">Procedure</h4>
          <p>
            1. Take a note of the leaf Provided. Note its shape and features on it by using a handlens.<br>
            2. Hold the magnifying lens with the right hand and the leaf with the left hand.<br/>
            3. Move the lens from the object towards the eye until a clear enlarged image is seen.<br/>
            4. Alternatively, Practice this with the leaf.<br/>
            5. Examine the leaf, noting it's external feature.<br/>
            6. Record your observations in form of a drawing. Label your diagram fully.<br/>
            7. State the magnification of your drawing. Magnification is a way of expressing how big or small you have made your drawing to the specimen.
        </p><br/>  
          
          <h4>Rules of Magnification</h4>
          <p>
         Unless otherwise instructed, measurement of the specimen size must be taken along the longest part. For circular specimens, the longest line passing through the centre must be used. A line must be drawn along a corresponding part on the drawing/diagram.  Measurements must be recorded to one decimal place if in centimetres or no decimal place if in millimetres e.g. it is correct to record 6.0 cm or 60 mm but wrong to record 6 cm or 60.0 mm. The following diagram illustrates a drawing of a transverse section of an orange, taking into account the principles discussed above. 
                 
          </p><br/>
