      <li>Only specified and small amounts of substances should be used in the reactions to avoid waste and reactions which cannot be controlled.</li>
      <li>Make sure that you know the substances being used unless you are told to use the substance as an unknown.</li>
      <li>Do not use broken glassware</li>
